import androidx.compose.ui.tooling.preview.Preview
import androidx.navigation.compose.currentBackStackEntryAsState
import androidx.navigation.compose.rememberNavController
import com.example.we8.ui.components.NavigationIcon
import com.example.we8.ui.navigation.ComposeNavigation
import com.example.we8.ui.theme.We8Theme
import dagger.hilt.android.AndroidEntryPoint

@AndroidEntryPoint
class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContent {
            val navController = rememberNavController()
            We8Theme {
                // A surface container using the 'background' color from the theme
                Surface(color = MaterialTheme.colors.background) {
                    Scaffold(
                        topBar = {
                            TopAppBar(
                                title = { Text("We8") },
                                navigationIcon = { NavigationIcon(navController) },
                                backgroundColor = Color.Blue,
                                contentColor = Color.White
                            )
                        },
                        content = {
                            ComposeNavigation(navController)
                        }
                    )

                }
            }
        }
    }
}

